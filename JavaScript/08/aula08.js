// pré e pós incremento ' ++ '
let n;
n=10;
++n // 11 pré incremento +1
console.log(n) //return 11
n++ //pós incremento +1
console.log(n) // return 12
// pré e pós decremento ' -- '
console.log(--n) //return 11
n-- 
console.log(n) //return 10
